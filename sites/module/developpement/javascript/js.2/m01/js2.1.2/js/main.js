let image = Array.from(document.querySelectorAll("#photo-list img"));
let nombrePhotoSelect = document.getElementById("nombrePhotoSelect");
let a = 0;

/*************************************************************************************************/
/* ***************************************** FONCTIONS ***************************************** */
/*************************************************************************************************/

function photoSelect(element) {
    element.classList.toggle("selected");
        
    nombrePhotoSelect.innerText = document.querySelectorAll(".selected").length;
}


function imageAnim() {
    image.forEach( image => {
        image.addEventListener("click", function() {
            photoSelect(this);
        });
    });
}

/*************************************************************************************************/
/* ************************************** CODE PRINCIPAL *************************************** */
/*************************************************************************************************/

imageAnim();



